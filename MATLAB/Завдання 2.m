clc;
clear;
close all;

%% ---------------------------------------------------------
% ЕКСПЕРИМЕНТ 2
% Адаптивна лінійна нейронна мережа
%% ---------------------------------------------------------

%% 1. Формування функції x(t)

Fs = 50;              % Частота дискретизації (50 вимірів/сек)
Tmax = 2.5;           % Тривалість сигналу

t = 0:1/Fs:Tmax;      % Часовий вектор

% Вхідна функція
x = sin(2*pi*t);

% Бажаний вихід
y = 2*x + 3;

%% Графік початкових сигналів
figure;
plot(t,x,'b','LineWidth',2);
hold on;
plot(t,y,'r','LineWidth',2);

grid on;
xlabel('Час, с');
ylabel('Амплітуда');
title('Вхідний та бажаний вихідний сигнали');

legend('x(t)=sin(2\pit)','y(t)=2x(t)+3');

%% ---------------------------------------------------------
% 2. Створення адаптивної лінійної мережі
%% ---------------------------------------------------------

delays = 1:2;     % Два блоки затримки

% Створення мережі
net = newlin(minmax(x),1,delays,0.01);

%% ---------------------------------------------------------
% 3. Підготовка даних та навчання мережі
%% ---------------------------------------------------------

[Xs,Xi,Ai,Ts] = preparets(net,num2cell(x),num2cell(y));

% Кількість епох навчання
net.trainParam.epochs = 300;

% Навчання мережі
net = train(net,Xs,Ts,Xi,Ai);

%% Робота мережі
Y = net(Xs,Xi,Ai);

% Перетворення у числовий формат
Ynum = cell2mat(Y);

% Цільові значення
Tnum = cell2mat(Ts);

%% ---------------------------------------------------------
% 4. Обчислення та побудова графіка помилки
%% ---------------------------------------------------------

% Помилка
E = Tnum - Ynum;

% Середньоквадратична помилка
perf = perform(net,Ts,Y);

disp(['Середньоквадратична помилка: ', num2str(perf)]);

%% Графік помилки
figure;

plot(E,'m','LineWidth',2);

grid on;
xlabel('Номер відліку');
ylabel('Помилка');
title('Помилка на виході нейронної мережі');

%% ---------------------------------------------------------
% Графік результатів роботи мережі
%% ---------------------------------------------------------

figure;

plot(Tnum,'b','LineWidth',2);
hold on;
plot(Ynum,'r--','LineWidth',2);

grid on;
xlabel('Номер відліку');
ylabel('Значення');

title('Порівняння бажаного та фактичного виходу');

legend('Бажаний вихід','Вихід мережі');

%% ---------------------------------------------------------
% 5. Дослідження впливу кількості блоків затримки
%% ---------------------------------------------------------

maxDelays = 10;

errors = zeros(1,maxDelays);

for d = 1:maxDelays

    % Створення мережі з d затримками
    netTest = newlin(minmax(x),1,1:d,0.01);

    % Підготовка даних
    [Xs,Xi,Ai,Ts] = preparets(netTest,num2cell(x),num2cell(y));

    % Навчання
    netTest.trainParam.epochs = 300;
    netTest = train(netTest,Xs,Ts,Xi,Ai);

    % Вихід мережі
    Y = netTest(Xs,Xi,Ai);

    % Помилка
    errors(d) = perform(netTest,Ts,Y);

    fprintf('Затримки: %d --> Помилка: %f\n', ...
        d, errors(d));
end

%% Пошук оптимальної кількості затримок

[minError,optDelay] = min(errors);

disp(' ');
disp(['Оптимальна кількість блоків затримки: ', ...
    num2str(optDelay)]);

disp(['Мінімальна помилка: ', ...
    num2str(minError)]);

%% ---------------------------------------------------------
% Графік залежності помилки від кількості затримок
%% ---------------------------------------------------------

figure;

plot(1:maxDelays,errors,'k-o','LineWidth',2);

grid on;

xlabel('Кількість блоків затримки');
ylabel('Середньоквадратична помилка');

title('Вплив кількості блоків затримки на точність мережі');