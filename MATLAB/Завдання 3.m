clc;
clear;
close all;

%% ---------------------------------------------------------
% ЕКСПЕРИМЕНТ
% Адаптивна лінійна нейронна мережа ADALINE
%% ---------------------------------------------------------

%% 1. Формування сигналу x(t)

%% Перші 3 секунди
Fs1 = 100;                 % Частота дискретизації
t1 = 0:1/Fs1:3;

x1 = sin(2*pi*t1);

%% Наступні 2 секунди
Fs2 = 40;                  % Нова частота дискретизації
t2 = 3+1/Fs2:1/Fs2:5;

% Збільшення частоти сигналу
x2 = sin(4*pi*t2);

%% Об'єднання сигналів
t = [t1 t2];
x = [x1 x2];

%% Побудова вхідного сигналу
figure;

plot(t,x,'b','LineWidth',1.5);

grid on;
xlabel('Час, с');
ylabel('Амплітуда');

title('Вхідний сигнал x(t)');

%% ---------------------------------------------------------
% 2. Створення адаптивної лінійної мережі
%% ---------------------------------------------------------

delays = 1:2;

% Створення мережі
net = newlin(minmax(x),1,delays,0.01);

%% ---------------------------------------------------------
% 3. Формування цільового сигналу
%% ---------------------------------------------------------

% Затримка сигналу на 1 крок
xDelayed = [0 x(1:end-1)];

% Цільовий сигнал
y = 3*xDelayed + 1;

%% Графік цільового сигналу
figure;

plot(t,y,'r','LineWidth',1.5);

grid on;
xlabel('Час, с');
ylabel('Амплітуда');

title('Цільовий сигнал y(t)');

%% ---------------------------------------------------------
% Підготовка даних
%% ---------------------------------------------------------

[Xs,Xi,Ai,Ts] = preparets(net,num2cell(x),num2cell(y));

%% ---------------------------------------------------------
% Навчання мережі
%% ---------------------------------------------------------

net.trainParam.epochs = 300;

net = train(net,Xs,Ts,Xi,Ai);

%% ---------------------------------------------------------
% Робота мережі
%% ---------------------------------------------------------

Y = net(Xs,Xi,Ai);

Ynum = cell2mat(Y);
Tnum = cell2mat(Ts);

%% ---------------------------------------------------------
% 4. Обчислення помилки
%% ---------------------------------------------------------

E = Tnum - Ynum;

% Середньоквадратична помилка
perf = perform(net,Ts,Y);

disp(['Середньоквадратична помилка: ', num2str(perf)]);

%% Графік помилки
figure;

plot(E,'m','LineWidth',1.5);

grid on;

xlabel('Номер відліку');
ylabel('Помилка');

title('Помилка на виході мережі');

%% ---------------------------------------------------------
% Порівняння виходів
%% ---------------------------------------------------------

figure;

plot(Tnum,'b','LineWidth',2);
hold on;

plot(Ynum,'r--','LineWidth',2);

grid on;

xlabel('Номер відліку');
ylabel('Значення');

title('Порівняння бажаного та фактичного виходу');

legend('Бажаний вихід','Вихід мережі');

%% ---------------------------------------------------------
% 5. Дослідження впливу кількості затримок
%% ---------------------------------------------------------

maxDelays = 10;

errors = zeros(1,maxDelays);

for d = 1:maxDelays

    % Створення мережі
    netTest = newlin(minmax(x),1,1:d,0.01);

    % Підготовка даних
    [Xs,Xi,Ai,Ts] = preparets(netTest,...
        num2cell(x),num2cell(y));

    % Навчання
    netTest.trainParam.epochs = 300;

    netTest = train(netTest,Xs,Ts,Xi,Ai);

    % Вихід мережі
    Y = netTest(Xs,Xi,Ai);

    % Обчислення помилки
    errors(d) = perform(netTest,Ts,Y);

    fprintf('Затримки: %d --> Помилка: %f\n',...
        d, errors(d));
end

%% ---------------------------------------------------------
% Пошук оптимальної кількості затримок
%% ---------------------------------------------------------

[minError,optDelay] = min(errors);

disp(' ');
disp(['Оптимальна кількість блоків затримки: ',...
    num2str(optDelay)]);

disp(['Мінімальна помилка: ',...
    num2str(minError)]);

%% ---------------------------------------------------------
% Графік залежності помилки
%% ---------------------------------------------------------

figure;

plot(1:maxDelays,errors,'k-o','LineWidth',2);

grid on;

xlabel('Кількість блоків затримки');
ylabel('Середньоквадратична помилка');

title('Вплив кількості блоків затримки на помилку');

%% ---------------------------------------------------------
% 6. Висновки
%% ---------------------------------------------------------

disp(' ');
disp('ВИСНОВКИ:');
disp('1. Лінія затримки дозволяє мережі враховувати');
disp('   попередні значення сигналу.');
disp('2. Зі збільшенням кількості блоків затримки');
disp('   мережа краще прогнозує часові залежності.');
disp('3. Надто велика кількість затримок може');
disp('   призвести до ускладнення моделі.');
disp('4. Існує оптимальна кількість затримок,');
disp('   при якій помилка мінімальна.');